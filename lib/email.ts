import nodemailer from 'nodemailer';

// ‡∏™‡∏£‡πâ‡∏≤‡∏á transporter (‡πÉ‡∏ä‡πâ Gmail ‡∏´‡∏£‡∏∑‡∏≠ SMTP service ‡∏≠‡∏∑‡πà‡∏ô)
const transporter = nodemailer.createTransport({
  host: process.env.SMTP_HOST || 'smtp.gmail.com',
  port: parseInt(process.env.SMTP_PORT || '587'),
  secure: process.env.SMTP_SECURE === 'true', // true for 465, false for 587
  auth: {
    user: process.env.SMTP_USER, // Gmail address ‡∏´‡∏£‡∏∑‡∏≠ SMTP user
    pass: process.env.SMTP_PASS, // Gmail app password ‡∏´‡∏£‡∏∑‡∏≠ SMTP password
  },
});

export interface EmailOptions {
  to: string;
  subject: string;
  html: string;
  text?: string;
}

export const sendEmail = async (options: EmailOptions): Promise<boolean> => {
  try {
    const info = await transporter.sendMail({
      from: `"${process.env.FROM_NAME || 'Learning System'}" <${process.env.FROM_EMAIL || process.env.SMTP_USER}>`,
      to: options.to,
      subject: options.subject,
      text: options.text,
      html: options.html,
    });

    console.log('Email sent: %s', info.messageId);
    return true;
  } catch (error) {
    console.error('Error sending email:', error);
    return false;
  }
};

